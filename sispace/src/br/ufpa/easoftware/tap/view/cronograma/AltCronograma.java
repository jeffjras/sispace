    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        Cronograma cronograma = new Cronograma();
        cronograma.setId(id);        
        cronograma.setDescricao(txtDescricao.getText());
        cronograma.setData(getParsedDate(txtData.getText()));        
        cronograma.setAnoLetivo(Integer.parseInt(txtAnoLetivo.getText()));
        cronograma.setStatus(1);        
        DAOCronograma daoCronograma = new DAOCronograma();
        try {
            daoCronograma.atualizar(cronograma);
            JOptionPane.showMessageDialog(null, "Cronograma alterado com sucesso!");   
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar atualizar o registro!" + e.getMessage());
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

